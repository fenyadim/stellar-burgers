describe('Тестирование редьюсеров burgerConstructor', () => {
  test('Тестирование редьюсера добавления ингредиента addIngredient', () => {
    const newState = reducer(initialState, addIngredient(mockIngiredient));
    const { ingredients } = newState;
    expect(ingredients).toEqual([mockIngiredient]);
  });

  test('Тестирование редьюсера удаление ингредиента removeIngredient', () => {
    const state = reducer(initialState, addIngredient(mockIngiredient));
    const { ingredients } = reducer(state, removeIngredient('12345'));
    expect(ingredients).toEqual([]);
  });

  test('Тестирование редьюсера перемещение ингредиента вниз moveIngredientDown', () => {
    const state = reducer(
      { ...initialState, ingredients: mockIngredients },
      moveIngredientDown(0)
    );
    const { ingredients } = state;
    expect(ingredients).toEqual(sortedIngredients);
  });
});
